import UIKit

extension UIColor {
    @nonobjc class var primaryDark: UIColor {
        return UIColor(red: 123.0 / 255.0, green: 31.0 / 255.0, blue: 162.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var primary: UIColor {
        return UIColor(red: 156.0 / 255.0, green: 39.0 / 255.0, blue: 176.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var textColor: UIColor {
        return UIColor(white: 0.0, alpha: 0.5)
    }
    
    @nonobjc class var borderColor: UIColor {
        return UIColor(red: 185.0 / 255.0, green: 175.0 / 255.0, blue: 187.0 / 255.0, alpha: 1.0)
    }
}

